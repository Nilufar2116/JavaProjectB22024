package day_23_decimal_format_arrays_loops_review;

public class LoopsReview {
    public static void main(String[] args) {

        int i =1;
        int j =5;

      do {

          while (i > j){
              System.out.println("Hello 1");
          }

          System.out.println("Hello 2");

      } while (i > j);


    }
}
