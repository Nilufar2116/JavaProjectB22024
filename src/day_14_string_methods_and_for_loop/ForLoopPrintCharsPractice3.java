package day_14_string_methods_and_for_loop;

public class ForLoopPrintCharsPractice3 {
    public static void main(String[] args) {
        // Write a program to print 'A' to Z using for loop
       for (char ch='A'; ch <='Z'; ch++){

           // if you want to print them in one line
           System.out.print(ch+" ");
           // if you want to print each character in a new line
           //System.out.println(ch);
       }

        System.out.println();

        System.out.println("=============================");

       // write a program to print 'a' to 'z' using for loop
        for (char ch ='a'; ch<='z'; ch++){
            // if you want to print them in one line
            System.out.print(ch+" ");
            // if you want to print each character in a new line
            //System.out.println(ch);
        }
    }
}
