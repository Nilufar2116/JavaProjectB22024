package day_43_oop_inheritance_continue;

public class SuperKeywords2 {

    public SuperKeywords2(){

        System.out.println("Super class's Default Constructor");
    }
    public SuperKeywords2(int num){
        System.out.println("Super class with int parameter Constructor");
        System.out.println(num);
    }
    public SuperKeywords2(String str){
        System.out.println("Super class with String parameter Constructor");
        System.out.println(str);
    }

}
